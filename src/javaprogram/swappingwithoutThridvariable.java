package javaprogram;

public class swappingwithoutThridvariable {
	public static void main(String[] args) {
		int a=10,b=5;
		System.out.println("Original Value");
		System.out.println(a);
		System.out.println(b);
		System.out.println("swap value");
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println(a);
		System.out.println(b);
		
	}

}
